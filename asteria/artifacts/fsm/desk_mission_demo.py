        #     failed: Say("Desk mission demo failed")
        
        # Code generated by genfsm on Mon Apr 20 16:41:12 2026:
        
        start = Say("Starting desk mission demo") .set_name("start") .set_parent(self)
        scan = SceneInventoryScan() .set_name("scan") .set_parent(self)
        summarize = self.SayLatestInventorySummary() .set_name("summarize") .set_parent(self)
        approach_operator = ApproachOperatorScan() .set_name("approach_operator") .set_parent(self)
        done = Say("Desk mission demo complete") .set_name("done") .set_parent(self)
        failed = Say("Desk mission demo failed") .set_name("failed") .set_parent(self)
        
